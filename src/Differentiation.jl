             index::Union{Int, Nothing}=nothing) where {T<:Number} -> 
    ParamBox{T, dataName, :itself}

    ParamBox(data::Array{T, 0}, name::Symbol, mapFunction::Function, 
             dataName::Symbol=:undef; canDiff::Bool=true, 
